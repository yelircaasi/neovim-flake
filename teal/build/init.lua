local path_helpers = {}
path_helpers.resolve = function(path) return vim.fn.expand(path) end
path_helpers.resolve_existing = function(path)
	local resolved = path_helpers.resolve(path)
	if not vim.fn.exists(resolved) then error("function'resolve': path " .. path .. " must already exist.") end return resolved end
path_helpers.resolve_and_mkdir = function(path)
	local resolved = path_helpers.resolve(path)
	if not vim.fn.exists(resolved) then vim.fn.mkdir(resolved) end
	return resolved
end
local prepend_safe = function(path, create)
	local resolver = create and path_helpers.resolve_and_mkdir or path_helpers.resolve_existing
	local expanded_path = resolver(path)
	if not vim.tbl_contains(vim.opt.runtimepath:get(), expanded_path) then vim.opt.runtimepath:prepend(expanded_path) end
end
